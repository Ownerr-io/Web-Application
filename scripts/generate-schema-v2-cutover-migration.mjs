/**
 * Generates supabase/migrations/20260702530000_schema_v2_full_cutover.sql
 * — rewrite function bodies to physical names, drop compat views, extra indexes.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const map = JSON.parse(
  fs.readFileSync(
    path.join(repoRoot, "scripts/schema-v2/table-renames.json"),
    "utf8",
  ),
);

const legacyToPhysical = Object.fromEntries(
  map.renames.map(([physical, legacy]) => [legacy, physical]),
);
const legacyNames = Object.keys(legacyToPhysical).sort(
  (a, b) => b.length - a.length,
);

function sqlQuote(s) {
  return s.replace(/'/g, "''");
}

const replacePairsSql = legacyNames
  .flatMap((legacy) => {
    const physical = legacyToPhysical[legacy];
    return [
      [`public.${legacy}`, `public.${physical}`],
      [`public.${legacy}%ROWTYPE`, `public.${physical}%ROWTYPE`],
    ];
  })
  .map(
    ([from, to]) =>
      `    newdef := replace(newdef, '${sqlQuote(from)}', '${sqlQuote(to)}');`,
  )
  .join("\n");

const dropViews = legacyNames
  .map((legacy) => `DROP VIEW IF EXISTS public.${legacy} CASCADE;`)
  .join("\n");

const out = `-- Schema v2 full cutover: physical names only (no compat views).
-- Generated by scripts/generate-schema-v2-cutover-migration.mjs

BEGIN;

-- Rewrite stored functions/procedures that still reference legacy relation names.
DO $$
DECLARE
  r record;
  def text;
  newdef text;
BEGIN
  FOR r IN
    SELECT p.oid
    FROM pg_proc p
    JOIN pg_namespace n ON n.oid = p.pronamespace
    WHERE n.nspname = 'public'
      AND p.prokind IN ('f', 'p')
  LOOP
    def := pg_get_functiondef(r.oid);
    newdef := def;
${replacePairsSql}
    IF newdef IS DISTINCT FROM def THEN
      BEGIN
        EXECUTE newdef;
      EXCEPTION
        WHEN OTHERS THEN
          RAISE WARNING 'schema_v2_cutover: could not rewrite oid %: %', r.oid, SQLERRM;
      END;
    END IF;
  END LOOP;
END $$;

-- Drop legacy compatibility views (API must use physical names).
${dropViews}

-- Scalability: hot-path indexes (idempotent).
CREATE INDEX IF NOT EXISTS catalog_listings_status_visibility_idx
  ON public.catalog_listings (status, visibility, updated_at DESC);
CREATE INDEX IF NOT EXISTS founder_campaign_submissions_auth_user_idx
  ON public.founder_campaign_submissions (auth_user_id, created_at DESC);
CREATE INDEX IF NOT EXISTS trust_verification_results_startup_created_idx
  ON public.trust_verification_results (startup_id, computed_at DESC);
CREATE INDEX IF NOT EXISTS marketplace_seller_publications_startup_idx
  ON public.marketplace_seller_publications (startup_id);

COMMENT ON SCHEMA public IS 'Schema v2 cutover: physical tables only; legacy views removed.';

NOTIFY pgrst, 'reload schema';

COMMIT;
`;

const dest = path.join(
  repoRoot,
  "supabase/migrations/20260702530000_schema_v2_full_cutover.sql",
);
fs.writeFileSync(dest, out);
console.log("Wrote", dest);
