/**
 * Generates supabase/migrations seed SQL from current mockStartups (one-time / regen).
 * Run: npx tsx scripts/generate-marketplace-seed-sql.mjs
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const { mockStartups } = await import('../src/lib/mockData.ts');

const root = join(dirname(fileURLToPath(import.meta.url)), '..', '..', '..');
const out = join(root, 'supabase', 'migrations', '20260601120100_marketplace_seed_data.sql');

function sqlStr(v) {
  if (v == null) return 'null';
  return `'${String(v).replace(/'/g, "''")}'`;
}

const lines = [
  '-- Generated by artifacts/ownerr-web-app/scripts/generate-marketplace-seed-sql.mjs',
  'begin;',
  "delete from public.startup_metrics where startup_id in (select id from public.startups where slug like 'seed-%' or slug in (select slug from public.startups));",
  'delete from public.startup_media where startup_id in (select id from public.startups);',
  'delete from public.seller_listings where startup_id in (select id from public.startups);',
  'delete from public.startups;',
];

for (const s of mockStartups) {
  const asking = s.price ?? null;
  const meta = {
    monthly_revenue_series: s.monthlyRevenueSeries,
    logo_color: s.logoColor,
    business_score: s.businessScore,
    lend_score: s.lendScore,
    acquisition_power: s.acquisitionPower,
    revenue_verified: s.revenueVerified,
    revenue_provider: s.revenueProvider,
    domain_verified: s.domainVerified,
    traffic_verified: s.trafficVerified,
    traffic_monthly_visitors: s.trafficMonthlyVisitors,
    traffic_trend: s.trafficTrend,
    listing_views: s.listingViews ?? 0,
    listing_favorites: s.listingFavorites ?? 0,
    mom_growth: s.momGrowth,
    customers: s.customers,
    peak_mrr: s.peakMrr ?? s.revenue,
    asking_price_strike: s.askingPriceStrike ?? null,
    revenue_growth_30d_pct: s.revenueGrowth30dPct ?? null,
    founder_display_name: s.founderDisplayName ?? null,
  };
  lines.push(`insert into public.startups (
    slug, founder_handle, title, tagline, description, industry, stage, asking_price, currency,
    annual_revenue, profit, growth_rate, team_size, founded_year, verified, visibility, status, metadata
  ) values (
    ${sqlStr(s.slug)},
    ${sqlStr(s.founderHandle)},
    ${sqlStr(s.name)},
    null,
    ${sqlStr(s.description)},
    ${sqlStr(s.category)},
    'live',
    ${asking ?? 'null'},
    'USD',
    ${s.revenue * 12},
    ${s.ttmProfit ?? 'null'},
    ${s.momGrowth ?? 'null'},
    ${s.customers ?? 'null'},
    ${s.foundedYear ?? 'null'},
    ${s.revenueVerified ? 'true' : 'false'},
    'public',
    ${s.forSale ? "'published'" : "'published'"},
    ${sqlStr(JSON.stringify(meta))}::jsonb
  ) on conflict (slug) do update set
    title = excluded.title,
    description = excluded.description,
    asking_price = excluded.asking_price,
    annual_revenue = excluded.annual_revenue,
    metadata = excluded.metadata,
    updated_at = now();`);
}

lines.push('commit;');
writeFileSync(out, lines.join('\n\n'));
console.log('Wrote', out, 'rows', mockStartups.length);
